Object.defineProperty(events, "keys", {
    get: function() {
        let allKeys = new GearArray<string>([]);
        for(let key in this) {
            if(key != "keys" && key != "cannotSetState") {
                allKeys.add(key.toLowerCase());
            }
        }
        return allKeys;
    },
    enumerable: false
});
Object.defineProperty(events, "cannotSetState", {
    get: function() {
        return new GearArray([
            "onafterrender",
            "onafterupdate"
        ]);
    },
    enumerable: false
});
